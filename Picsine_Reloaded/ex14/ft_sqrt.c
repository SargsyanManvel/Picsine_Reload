int	ft_sqrt(int nb)
{
	int	i;
	int	half;

	i = 1;
	half = nb / 2;
	if (nb < 0)
		return (0);
	while (i <= half)
	{
		if (i * i == nb)
			return (i);
		++i;
	}
	return (0);
}
